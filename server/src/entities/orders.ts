import { integer, pgTable, text, timestamp, varchar } from 'drizzle-orm/pg-core'

export const ordersTable = pgTable('orders', {
  id: integer().primaryKey().generatedAlwaysAsIdentity(),
  email: varchar({ length: 255 }).notNull(),
  phone: varchar({ length: 50 }).notNull(),
  firstName: varchar({ length: 100 }).notNull(),
  lastName: varchar({ length: 100 }).notNull(),
  address: varchar({ length: 255 }),
  apartment: varchar({ length: 100 }),
  city: varchar({ length: 100 }),
  province: varchar({ length: 100 }),
  zipCode: varchar({ length: 20 }),
  shippingMethod: varchar({ length: 50 }).default('pickup'),
  paymentMethod: varchar({ length: 50 }),
  subtotal: integer().notNull(),
  shippingCost: integer().notNull().default(0),
  tax: integer().notNull(),
  total: integer().notNull(),
  status: varchar({ length: 50 }).notNull().default('pending'),
  orderText: text().notNull(),
  createdAt: timestamp({ withTimezone: true }).notNull().defaultNow(),
  updatedAt: timestamp({ withTimezone: true }).notNull().defaultNow(),
  deletedAt: timestamp({ withTimezone: true }),
})
